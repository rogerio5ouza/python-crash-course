# The Zen of Python, by Tim Peters

# Beautiful is better than ugly. / Bonito é melhor que feio.
# Explicit is better than implicit. / Explícito é melhor do que implícito.
# Simple is better than complex. / Simples é melhor que complexo.
# Complex is better than complicated. / Complexo é melhor que complicado.
# Flat is better than nested. / Flat é melhor do que aninhado.
# Sparse is better than dense. / Esparso é melhor do que denso.
# Readability counts. / Contagem de legibilidade.
# Special cases aren't special enough to break the rules. / Casos especiais não são especiais o suficiente para quebrar as regras.
# Although practicality beats purity. / Embora a praticidade supere a pureza.
# Errors should never pass silently. / Os erros nunca devem passar silenciosamente.
# Unless explicitly silenced. / A menos que seja explicitamente silenciado.
# In the face of ambiguity, refuse the temptation to guess. / Diante da ambiguidade, recuse a tentação de advinhar.
# There should be one-- and preferably only one --obvious way to do it. / Deve-se haver uma - e de preferência apenas uma - maneira óbvia de fazer isso.
# Although that way may not be obvious at first unless you're Dutch. / Embora esse caminho possa não ser óbvio no início, a menos que você seja holandês.
# Now is better than never. / Agora é melhor do que nunca.
# Although never is often better than *right* now. / Embora nunca seja sempre melhor do que *agora* agora.
# If the implementation is hard to explain, it's a bad idea. / Se a implementação for difícil de explicar, é uma má idéia.
# If the implementation is easy to explain, it may be a good idea. / Se a implementação for fácil de explicar, pode ser uma boa ideia.
# Namespaces are one honking great idea -- let's do more of those! / Namespaces são uma ótima ideia -  vamos fazer mais deles!
